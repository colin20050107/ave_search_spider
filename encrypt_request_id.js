/**
 * 使用 RSA-OAEP SHA-256 加密消息
 * @param {string} message - 待加密的明文
 * @returns {Promise<string>} Base64 编码的密文
 */
async function encryptRequestId(message) {
  // 导入公钥 (SPKI/DER 格式)
  const publicKey = await crypto.subtle.importKey(
    'spki',
    PUBLIC_KEY_DER,
    {
      name: 'RSA-OAEP',
      hash: 'SHA-256',
    },
    true,
    ['encrypt']
  );

  // 编码消息
  const encoder = new TextEncoder();
  const encoded = encoder.encode(message);

  // RSA-OAEP 加密
  const encrypted = await crypto.subtle.encrypt(
    { name: 'RSA-OAEP' },
    publicKey,
    encoded
  );

  // Base64 编码
  return Buffer.from(encrypted).toString('base64');
}

/**
 * 生成 MurmurHash3 x64 128-bit (简化版 - 用于生成 visitor ID)
 * 这里我们使用 crypto 的随机数生成一个 visitor ID
 * 实际的 visitor ID 由浏览器指纹计算，但服务器主要用其做辨识
 */
function generateVisitorId() {
  // 生成 32 字符的 hex 字符串，模拟 FingerprintJS 的 visitorId
  const bytes = crypto.randomBytes(16);
  return bytes.toString('hex');
}

/**
 * 构建完整的 request_id
 * @param {string} visitorId - 访客标识
 * @param {string} platform - 平台: "web" 或 "h5"
 * @param {number|string} powValue - 来自 serverTime API 的值
 * @returns {Promise<string>}
 */
async function buildRequestId(visitorId, platform, powValue) {
  const message = visitorId + '$' + platform + '$1.0.0$r1r$' + powValue;
  return await encryptRequestId(message);
}

